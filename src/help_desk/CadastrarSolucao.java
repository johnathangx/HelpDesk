    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(descricaoTextField.getText().equals("") || solucaoTextArea.getText().equals("")){
            JOptionPane.showMessageDialog(rootPane, "Existem informações que não foram preenchidas, por favor verificar!");
        }        
        else{
            try {

            Class.forName("com.mysql.jdbc.Driver");
            
            Connection con;
 
            con = (Connection) DriverManager.getConnection("jdbc:mysql://localhost:3306/helpdesk?zeroDateTimeBehavior=convertToNull", "root", "12345$$");
 

            String query = "INSERT INTO baseconhecimento (problema,solucaoProblema)"
                    + "values (?,?)";
            PreparedStatement stmt = (PreparedStatement) con.prepareStatement(query);
                    stmt.setString(1, descricaoTextField.getText());
                    stmt.setString(2, solucaoTextArea.getText());
                    stmt.executeUpdate();
                    
                    stmt.close();
                    con.close();
 
            JOptionPane.showMessageDialog(rootPane, "Solução Cadastrada.");
            new TelaMenu().setVisible(true);
            dispose();
        } catch (SQLException | ClassNotFoundException e) {
            JOptionPane.showMessageDialog(rootPane, e);
        }
    }
    }//GEN-LAST:event_jButton1ActionPerformed


